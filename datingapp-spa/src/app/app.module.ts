export function tokenGetter() {
   return localStorage.getItem('token');
}
export class CustomHammerConfig extends HammerGestureConfig  {
   overrides = {
       pinch: { enable: false },
       rotate: { enable: false }
   };
}
@NgModule({
   declarations: [
      AppComponent,
      ValueComponent,
      NavComponent,
      HomeComponent,
      RegisterComponent,
      MemberListComponent,
      ListsComponent,
      MessagesComponent,
      MemberCardComponent,
      MemberDetailsComponent
   ],
   imports: [
      BrowserModule,
      AppRoutingModule,
      HttpClientModule,
      FormsModule,
      BrowserAnimationsModule,
      TabsModule.forRoot(),
      BsDropdownModule.forRoot(),
      NgxGalleryModule,
      JwtModule.forRoot({
         config: {
           tokenGetter,
           whitelistedDomains: ['localhost:5000'],
           blacklistedRoutes: ['localhost:5000/auth/']
         }
       })
   ],
   providers: [
      AuthService,
      AlertifyService,
      ErrorInterceptorProvider,
      MemberDetailResolver,
      MemberListResolver,
      {
         provide: HAMMER_GESTURE_CONFIG, useClass: CustomHammerConfig
     }
   ],
   bootstrap: [
      AppComponent
   ]
})
export class AppModule { }
